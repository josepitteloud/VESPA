/*###############################################################################
#p_personal_income_band_v2   Personal Income Band V2
#
#Personal Income band identifies an individual’s likely income within specified ranges.  
#
###############################################################################*/

-- ##############################################################################################################
-- ##### STEP 1.0 - Basic Select									    #####
-- ##############################################################################################################


select top 100 cb_key_household, 
case p_personal_income_band_v2 
when  '0' then  '< £10,000'
when '1'    then '£10,000 - £14,999'
when '2'    then '£15,000 - £19,999'
when '3'    then '£20,000 - £29,999'
when '4'    then '£30,000 - £39,999'
when '5'    then '£40,000 - £49,999'
when '6'    then '£50,000 - £59,999'
when '7'    then '£60,000 - £69,999'
when '8'    then '£70,000 - £99,999'
when '9'    then '£100,000 +'
when 'U'    then 'Unallocated'
else null end 'Personal Income Band V2'
from sk_prod.experian_consumerview
where p_personal_income_band_v2 is not null


