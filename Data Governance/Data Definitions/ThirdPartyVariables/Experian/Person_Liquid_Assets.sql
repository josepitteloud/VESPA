/*###############################################################################
#p_liquid_assets	Liquid Assets	
#
#Liquid Assets identifies the value of assets which can be immediately accessed, such as current accounts
#
###############################################################################*/


-- ##############################################################################################################
-- ##### STEP 1.0 - Basic Select									    #####
-- ##############################################################################################################


select top 100 cb_key_household, 
case person_liquid_assets 
when '0' then 'Low value (<£500)'
when '1' then 'Medium value (£500-£5,000)'
when '2' then 'High value (£5,000-£20,000)'
when '3' then 'Very high value (£20,000+)'
when 'U' then 'Unclassified'
else null end 'Liquid Assets'
from sk_prod.experian_consumerview
where person_liquid_assets is not null


