/*###############################################################################
#p_shareholding_value	Person shareholding value band	
#
#Shareholding Value identifies the value of shares held by each individual living at an address.
#
###############################################################################*/


-- ##############################################################################################################
-- ##### STEP 1.0 - Basic Select									    #####
-- ##############################################################################################################


select top 100 cb_key_household, 
case person_shareholding_value 
when  '0' then 'No shares'
when '1' then 'Low value (<£10,000)'
when '2' then 'High value (>£10,000)'
when 'U' then 'Unclassified'
else null end 'Person Shareholding Value' 
from sk_prod.experian_consumerview
where person_shareholding_value is not null

