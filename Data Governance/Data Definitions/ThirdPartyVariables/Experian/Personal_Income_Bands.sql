/*###############################################################################
#p_personal_income_band	Personal income band	
#
#Personal Income band identifies an individual’s likely income with in specified ranges.  
#
###############################################################################*/


-- ##############################################################################################################
-- ##### STEP 1.0 - Basic Select									    #####
-- ##############################################################################################################


select top 100 cb_key_household,
case 
personal_income_bands 
when '0' then '< £5,000'
when '1' then '£5,000 - £7,999'
when '2' then '£8,000 - £11,999'
when '3' then '£12,000 - £14,999'
when '4' then '£15,000 - £19,999'
when '5' then '£20,000 - £24,999'
when '6' then '£25,000 - £29,999'
when '7' then '£30,000 - £39,999'
when '8' then '£40,000 - £49,999'
when '9' then '£50,000 +'
when 'U' then 'Unclassified'
else null end 'Personal Income Bands'
from sk_prod.experian_consumerview
where personal_income_bands is not null

