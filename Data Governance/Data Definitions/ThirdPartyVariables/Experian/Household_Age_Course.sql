/*###############################################################################
#h_age_coarse
#
#Household Age model, coarse values
#
#At the household level,
#Age is based on the individual age estimate of the head of household
###############################################################################*/

-- ##############################################################################################################
-- ##### STEP 1.0 - Basic Select									    #####
-- ##############################################################################################################


select top 100 cb_key_household,
case household_age
when '0' then 'Age 18-25'
when '1' then 'Age 26-35'
when '2' then 'Age 36-45'
when '3' then 'Age 46-55'
when '4' then 'Age 56-65'
when '5' then 'Age 66+'
when 'U' then 'Unclassified'
else null end household_age
from sk_prod.experian_consumerview
where household_age is not null


