--sample code

select cb_key_household, model_score as CQM_Score
into #temp_cqm_score
from sk_prod.ID_V_universe_all;


select top 10 cb_key_household,
Case when CQM_score <=6  then    '1-6'
                             when CQM_score <=12 then    '7-12'
                             when CQM_score <=18 then    '13-18'
                             when CQM_score <=24 then    '19-24'
                             when CQM_score <=30 then    '25-30'
                             when CQM_score <=36 then    '31-36'
                             else                        NULL
                        end olive_cqm
from #temp_cqm_score


