---------------------------------------------------------------------------------
--  VESPA flag  [VESPA_PANEL_MEMBER]
---------------------------------------------------------------------------------

select distinct (account_number),panel_id_vespa
into #panel_ids
from Vespa_Analysts.Vespa_Single_Box_View;

SELECT DISTINCT account_number, 1 AS VESPA
INTO #vespa
FROM #panel_ids
WHERE panel_id_vespa in (12);


drop table #vespa;
drop table #panel_ids;
commit;

